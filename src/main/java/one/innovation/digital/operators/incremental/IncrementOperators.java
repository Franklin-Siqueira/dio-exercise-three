package one.innovation.digital.operators.incremental;

public class IncrementOperators {
    public static void main(String[] args) {
        // Initialize
        var newNumber = 1;
        // Print
        System.out.println(++newNumber);
        // Initialize
        var newValue = 10;
        // Print
        System.out.println(newValue--);
        System.out.println(newValue);
    }
}
